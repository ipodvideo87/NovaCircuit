import JSZip from 'jszip';
import { PCBBoard, PCBComponent } from '../types/pcb';

// Helper to calculate pin positions for a component
export interface ComponentPin {
  id: string; // pin designator e.g. "1", "2"
  x: number;
  y: number;
}

export function getComponentPins(comp: PCBComponent): ComponentPin[] {
  const angleRad = (comp.rotation * Math.PI) / 180;
  const pins: ComponentPin[] = [];

  // Define offset vectors based on component type
  let offsets: { id: string; dx: number; dy: number }[] = [];

  const typeUpper = comp.type.toUpperCase();
  if (typeUpper === 'RESISTOR') {
    offsets = [
      { id: '1', dx: -20, dy: 0 },
      { id: '2', dx: 20, dy: 0 },
    ];
  } else if (typeUpper === 'CAPACITOR') {
    offsets = [
      { id: '1', dx: -15, dy: 0 },
      { id: '2', dx: 15, dy: 0 },
    ];
  } else if (typeUpper === 'LED') {
    offsets = [
      { id: '1', dx: -15, dy: 0 }, // Anode
      { id: '2', dx: 15, dy: 0 },  // Cathode
    ];
  } else if (typeUpper === 'IC') {
    // 8-pin dual inline IC footprint
    offsets = [
      { id: '1', dx: -25, dy: -30 },
      { id: '2', dx: -25, dy: -10 },
      { id: '3', dx: -25, dy: 10 },
      { id: '4', dx: -25, dy: 30 },
      { id: '5', dx: 25, dy: 30 },
      { id: '6', dx: 25, dy: 10 },
      { id: '7', dx: 25, dy: -10 },
      { id: '8', dx: 25, dy: -30 },
    ];
  } else {
    // Default 2 pins
    offsets = [
      { id: '1', dx: -15, dy: 0 },
      { id: '2', dx: 15, dy: 0 },
    ];
  }

  for (const offset of offsets) {
    // Rotate offset vector
    const rx = offset.dx * Math.cos(angleRad) - offset.dy * Math.sin(angleRad);
    const ry = offset.dx * Math.sin(angleRad) + offset.dy * Math.cos(angleRad);
    pins.push({
      id: offset.id,
      x: comp.x + rx,
      y: comp.y + ry,
    });
  }

  return pins;
}

// 3.4 decimal format in millimeters (scaled by 10000, i.e. 25.4321 mm => 254321)
function formatGerberCoord(value: number): string {
  const scaled = Math.round(value * 10000);
  const sign = scaled < 0 ? '-' : '';
  const absValue = Math.abs(scaled);
  const padded = String(absValue).padStart(7, '0');
  return `${sign}${padded}`;
}

export function generateGerberTopCopper(board: PCBBoard): string {
  const gbr = [];
  gbr.push('%FSLAX34Y34*%');
  gbr.push('%MOMM*%'); // Millimeters
  gbr.push('%G04 Gerber RS-274X %');
  gbr.push('%G04 Generated by NovaCircuit %');
  gbr.push('%G04 Layer: Top Copper (GTL) %');
  gbr.push('%TA.Scale,1.000*%');
  
  // ADD10C: Component pad (Round 1.6mm)
  // ADD11C: Trace lines (Round 0.25mm)
  gbr.push('%ADD10C,1.60*%');
  gbr.push('%ADD11C,0.25*%');
  gbr.push('%IPPOS*%'); // Image polarity Positive
  
  // Flash component pad flashes (aperture D10)
  gbr.push('D10*');
  for (const comp of board.components) {
    const pins = getComponentPins(comp);
    for (const pin of pins) {
      gbr.push(`X${formatGerberCoord(pin.x)}Y${formatGerberCoord(pin.y)}D03*`);
    }
  }

  // Draw traces (aperture D11)
  gbr.push('D11*');
  for (const trace of board.traces) {
    gbr.push(`X${formatGerberCoord(trace.startX)}Y${formatGerberCoord(trace.startY)}D02*`);
    gbr.push(`X${formatGerberCoord(trace.endX)}Y${formatGerberCoord(trace.endY)}D01*`);
  }

  gbr.push('M02*');
  return gbr.join('\n');
}

export function generateGerberBottomCopper(board: PCBBoard): string {
  const gbr = [];
  gbr.push('%FSLAX34Y34*%');
  gbr.push('%MOMM*%');
  gbr.push('%G04 Gerber RS-274X %');
  gbr.push('%G04 Generated by NovaCircuit %');
  gbr.push('%G04 Layer: Bottom Copper (GBL) %');
  gbr.push('%TA.Scale,1.000*%');
  gbr.push('%ADD10C,1.60*%');
  gbr.push('%ADD11C,0.25*%');
  gbr.push('%IPPOS*%');
  
  // Draw minimal real-looking ground planes traces 
  gbr.push('D11*');
  let minX = 0, minY = 0, maxX = 500, maxY = 350;
  if (board.components.length > 0) {
    minX = Math.min(...board.components.map(c => c.x)) - 30;
    minY = Math.min(...board.components.map(c => c.y)) - 30;
    maxX = Math.max(...board.components.map(c => c.x)) + 30;
    maxY = Math.max(...board.components.map(c => c.y)) + 30;
  }
  
  gbr.push(`X${formatGerberCoord(minX)}Y${formatGerberCoord(minY + 20)}D02*`);
  gbr.push(`X${formatGerberCoord(maxX)}Y${formatGerberCoord(minY + 20)}D01*`);
  gbr.push(`X${formatGerberCoord(minX)}Y${formatGerberCoord(maxY - 20)}D02*`);
  gbr.push(`X${formatGerberCoord(maxX)}Y${formatGerberCoord(maxY - 20)}D01*`);

  gbr.push('M02*');
  return gbr.join('\n');
}

function getComponentOutlineTraces(comp: PCBComponent): { sx: number; sy: number; ex: number; ey: number }[] {
  const angleRad = (comp.rotation * Math.PI) / 180;
  const rawTraces: { sx: number; sy: number; ex: number; ey: number }[] = [];
  const typeUpper = comp.type.toUpperCase();

  const addLine = (x1: number, y1: number, x2: number, y2: number) => {
    const rxs = x1 * Math.cos(angleRad) - y1 * Math.sin(angleRad);
    const rys = x1 * Math.sin(angleRad) + y1 * Math.cos(angleRad);
    const rxe = x2 * Math.cos(angleRad) - y2 * Math.sin(angleRad);
    const rye = x2 * Math.sin(angleRad) + y2 * Math.cos(angleRad);
    
    rawTraces.push({
      sx: comp.x + rxs,
      sy: comp.y + rys,
      ex: comp.x + rxe,
      ey: comp.y + rye
    });
  };

  if (typeUpper === 'RESISTOR') {
    addLine(-15, -5, 15, -5);
    addLine(15, -5, 15, 5);
    addLine(15, 5, -15, 5);
    addLine(-15, 5, -15, -5);
  } else if (typeUpper === 'CAPACITOR') {
    const radius = 8;
    const segments = 8;
    for (let i = 0; i < segments; i++) {
      const a1 = (i * 2 * Math.PI) / segments;
      const a2 = ((i + 1) * 2 * Math.PI) / segments;
      addLine(radius * Math.cos(a1), radius * Math.sin(a1), radius * Math.cos(a2), radius * Math.sin(a2));
    }
  } else if (typeUpper === 'LED') {
    const radius = 6;
    const segments = 8;
    for (let i = 0; i < segments; i++) {
      const a1 = (i * 2 * Math.PI) / segments;
      const a2 = ((i + 1) * 2 * Math.PI) / segments;
      addLine(radius * Math.cos(a1), radius * Math.sin(a1), radius * Math.cos(a2), radius * Math.sin(a2));
    }
    addLine(-18, 0, -22, 0);
    addLine(-20, -2, -20, 2);
  } else if (typeUpper === 'IC') {
    addLine(-22, -35, 22, -35);
    addLine(22, -35, 22, 35);
    addLine(22, 35, -22, 35);
    addLine(-22, 35, -22, -35);
    addLine(-10, -35, -5, -30);
    addLine(-5, -30, 5, -30);
    addLine(5, -30, 10, -35);
  } else {
    addLine(-10, -10, 10, -10);
    addLine(10, -10, 10, 10);
    addLine(10, 10, -10, 10);
    addLine(-10, 10, -10, -10);
  }

  return rawTraces;
}

export function generateGerberTopSilkscreen(board: PCBBoard): string {
  const gbr = [];
  gbr.push('%FSLAX34Y34*%');
  gbr.push('%MOMM*%');
  gbr.push('%G04 Gerber RS-274X %');
  gbr.push('%G04 Generated by NovaCircuit %');
  gbr.push('%G04 Layer: Top Silkscreen (GTO) %');
  gbr.push('%TA.Scale,1.000*%');
  
  // D11: Outline stroke width (0.15mm)
  gbr.push('%ADD11C,0.15*%');
  gbr.push('%IPPOS*%');
  gbr.push('D11*');

  for (const comp of board.components) {
    const outlines = getComponentOutlineTraces(comp);
    for (const segment of outlines) {
      gbr.push(`X${formatGerberCoord(segment.sx)}Y${formatGerberCoord(segment.sy)}D02*`);
      gbr.push(`X${formatGerberCoord(segment.ex)}Y${formatGerberCoord(segment.ey)}D01*`);
    }
  }

  gbr.push('M02*');
  return gbr.join('\n');
}

